package git.Easy.LongestPalindromeSubString;

public class FindOccurence {
    public static void main(String[] args) {
        String A="bobabtbobl";
        int occurence=0;
        for(int i=0;i<A.length()-2;i++){
            if(A.substring(i,i+3).equals("bob")){
                occurence++;
            }
        }
        Long.parseLong("1",2);
        Long.toBinaryString(2);
        System.out.println(A.substring(0,3)==("bob"));
    }
}
